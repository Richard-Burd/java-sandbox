public class JavaLessonFourDoWhileLoop {

  public static void main(String[] args)
  {
    // Here is the format:
    // for (declare iterator; conditional statement; change iterator)
    for (int l=10; l >=1; l--) //int is declared inside here, but you could do it above...see forLoopAlt.java
    {
      System.out.println(l);
    }
  }

}
